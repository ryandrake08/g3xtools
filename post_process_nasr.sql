-- Create tables for connected segment data
CREATE TABLE AWY_LINES AS SELECT DISTINCT AWY_LOCATION, AWY_ID, LINE_SEQ, GROUP_CONCAT(FROM_POINT ORDER BY POINT_SEQ) AS WPT_IDS FROM AWY_SEG_ALT GROUP BY AWY_LOCATION, AWY_ID, LINE_SEQ;
CREATE TABLE DP_LINES AS SELECT DISTINCT DP_COMPUTER_CODE, ROUTE_NAME, BODY_SEQ, GROUP_CONCAT(POINT ORDER BY POINT_SEQ) AS WPT_IDS FROM DP_RTE GROUP BY DP_COMPUTER_CODE, ROUTE_NAME, BODY_SEQ;
CREATE TABLE MTR_LINES AS SELECT DISTINCT ROUTE_TYPE_CODE, ROUTE_ID, GROUP_CONCAT(ROUTE_PT_ID ORDER BY ROUTE_PT_SEQ) AS ROUTE_IDS FROM MTR_PT GROUP BY ROUTE_TYPE_CODE, ROUTE_ID;
CREATE TABLE STAR_LINES AS SELECT DISTINCT STAR_COMPUTER_CODE, ROUTE_NAME, BODY_SEQ, GROUP_CONCAT(POINT ORDER BY POINT_SEQ) AS WPT_IDS FROM STAR_RTE GROUP BY STAR_COMPUTER_CODE, ROUTE_NAME, BODY_SEQ;
